class TenTables
{
public static void main (String[] args)
{
for (int i=1; i<=10; i++)
{
System.out.println ("10*" +i+ "=" +10*i);
}
System.out.println ();
int j=1;
while (j<=10)
{
System.out.println ("3*" +j+ "=" +3*j);
j++;
}
System.out.println ();
int k=1;
do
{
System.out.println ("4*" +k+ "=" +4*k);
k++;
}
while (k<=10);
}
}


